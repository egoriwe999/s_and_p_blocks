def s_block_forward(input_data):
    table = [
        [0x1, 0x0, 0x3, 0x2, 0x5, 0x4, 0x7, 0x6],
        [0x6, 0x7, 0x4, 0x5, 0x2, 0x3, 0x0, 0x1],
        [0x3, 0x2, 0x1, 0x0, 0x7, 0x6, 0x5, 0x4],
        [0x4, 0x5, 0x6, 0x7, 0x0, 0x1, 0x2, 0x3]
    ]
    
    for i in range(2):
        t1 = (input_data & 0xF0) >> (4 * i)  # first tetrad
        t2 = input_data & 0x0F  # second tetrad
    
    output_data = (table[i][t1] << 4) | table[i][t2]
    return output_data

def s_block_inverse(input_data):
    table_inverse = [
        [0x1, 0x0, 0x3, 0x2, 0x5, 0x4, 0x7, 0x6],
        [0x2, 0x3, 0x0, 0x1, 0x6, 0x7, 0x4, 0x5],
        [0x3, 0x2, 0x1, 0x0, 0x7, 0x6, 0x5, 0x4],
        [0x6, 0x7, 0x4, 0x5, 0x2, 0x3, 0x0, 0x1]
    ]
    
    t1 = (input_data & 0xF0) >> 4  # перша тетрада
    t2 = input_data & 0x0F  # друга тетрада
    
    output_data = (table_inverse[0][t1] << 4) | table_inverse[1][t2]
    return output_data

